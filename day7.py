#what is if-else in python?
'''if-else is a conditional statement that allows you to execute different blocks of code based on certain conditions.'''
#Example:
x = 5
if x > 0:
    print("Positive")
else:
    print("Negative or Zero")
#prints: Positive

#what are loops in python?
'''Loops are used to execute a block of code repeatedly as long as a certain condition is met.'''
#Example of a for loop:
for i in range(5):
    print(i)    
#prints: 0 1 2 3 4 (each on a new line)

#Example of a while loop:
count = 0
while count < 5:
    print(count)
    count += 1
#prints: 0 1 2 3 4 (each on a new line)